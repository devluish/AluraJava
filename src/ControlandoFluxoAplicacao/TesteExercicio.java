package ControlandoFluxoAplicacao;

public class TesteExercicio {
    public static void main(String[] args) {
        int contador = 1;

        // Esse exemplo o contador fica adicionando +1 até encerrar o programa com o (variável++) -> sempre adiciona +1.
        while(contador <= 10) {
            System.out.println(contador);
            contador++;
        }
    }

}
